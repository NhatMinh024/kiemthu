    private void themKhoa() {
        String maKhoa = txtMaKHoa.getText().trim();
        String tenKhoa = txtTenKhoa.getText().trim();
        String ngayThanhLap = txtNgThLap.getText().trim();
        String truongKhoa = txtTKhoa.getText().trim();
        String sdt = txtSDTT.getText().trim();
        String email = txtEmail.getText().trim();
        String diaChi = txtDC.getText().trim();
        String ghiChu = txtGhiChu.getText().trim();
        int trangThai = jRadioButton1.isSelected() ? 0 : 1;

        // 1. Kiểm tra rỗng tất cả các trường
        if (maKhoa.isEmpty() || tenKhoa.isEmpty() || ngayThanhLap.isEmpty()
                || truongKhoa.isEmpty() || sdt.isEmpty() || email.isEmpty()
                || diaChi.isEmpty() || ghiChu.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Không được để trống bất kỳ thông tin nào!");
            return;
        }

        // 2. Kiểm tra mã khoa đã tồn tại
        if (!khoaDao.selectByMaKhoa(maKhoa).isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Mã khoa đã tồn tại!");
            return;
        }

        // 3. Kiểm tra định dạng ngày (dd-MM-yyyy)
        if (!ngayThanhLap.matches("\\d{2}-\\d{2}-\\d{4}")) {
            javax.swing.JOptionPane.showMessageDialog(this, "Ngày thành lập phải đúng định dạng Ngày-Tháng-Năm (dd-MM-yyyy)!");
            return;
        }

        // 3.1. Chuyển đổi sang yyyy-MM-dd để lưu vào DB
        String ngayThanhLapSql;
        try {
            java.text.SimpleDateFormat inputFormat = new java.text.SimpleDateFormat("dd-MM-yyyy");
            java.text.SimpleDateFormat sqlFormat = new java.text.SimpleDateFormat("yyyy-MM-dd");
            java.util.Date date = inputFormat.parse(ngayThanhLap);
            ngayThanhLapSql = sqlFormat.format(date);
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Ngày thành lập không hợp lệ!");
            return;
        }

        // 4. Kiểm tra định dạng email
        if (!email.matches("^[\\w.-]+@[\\w.-]+\\.[a-zA-Z]{2,}$")) {
            javax.swing.JOptionPane.showMessageDialog(this, "Email không hợp lệ!");
            return;
        }
        // SDT
        if (!sdt.matches("\\d{10}")) {
            javax.swing.JOptionPane.showMessageDialog(this, "Số điện thoại phải đúng 10 số!");
            return;
        }

        // 5. Thêm khoa mới (dùng ngày đã chuyển đổi)
        Khoa khoa = new Khoa(maKhoa, tenKhoa, ngayThanhLapSql, truongKhoa, sdt, email, diaChi, ghiChu, trangThai);
        khoaDao.insert(khoa);
        javax.swing.JOptionPane.showMessageDialog(this, "Thêm khoa thành công!");
        loadTableKhoa();
        btnClean.doClick();
    }

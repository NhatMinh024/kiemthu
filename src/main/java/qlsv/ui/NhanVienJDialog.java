private void sua() {
    int row = tblBangNV.getSelectedRow();
    if (row == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng chọn nhân viên cần sửa!");
        return;
    }
    // Kiểm tra thông tin không được rỗng
    if (txtMaNV.getText().trim().isEmpty() ||
        txtMatKhau.getText().trim().isEmpty() ||
        txtHoTen.getText().trim().isEmpty() ||
        txtEmail.getText().trim().isEmpty() ||
        txtSDT.getText().trim().isEmpty() ||
        txtDiaChi.getText().trim().isEmpty() ||
        txtNgayTao.getText().trim().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Không được để trống thông tin!");
        return;
    }
    // Chuyển đổi ngày tạo từ String sang java.sql.Date
    Date ngayTao;
    try {
        ngayTao = Date.valueOf(txtNgayTao.getText().trim()); // Định dạng yyyy-MM-dd
    } catch (Exception ex) {
        javax.swing.JOptionPane.showMessageDialog(this, "Ngày tạo không đúng định dạng (Năm-Tháng-Ngày)!");
        return;
    }

    // Tạo đối tượng nhân viên
    NhanVien nv = new NhanVien();
    nv.setMaNV(txtMaNV.getText().trim());
    nv.setMatKhau(txtMatKhau.getText().trim());
    nv.setHoTen(txtHoTen.getText().trim());
    nv.setEmail(txtEmail.getText().trim());
    nv.setSdt(txtSDT.getText().trim());
    nv.setDiaChi(txtDiaChi.getText().trim());
    nv.setNgayTao(ngayTao); // Đúng kiểu java.sql.Date
    nv.setGioiTinh("Nam".equals(cboGioiTinh.getSelectedItem()));
    nv.setVaiTro(rdQuanTri.isSelected());
    nv.setTrangThai(rdHoatDong.isSelected());

    NhanVienDAO dao = new NhanVienDAOImpl();
    dao.update(nv);// gọi dao để cập nhật trên database 
    fillTableNhanVien();
    javax.swing.JOptionPane.showMessageDialog(this, "Sửa thông tin nhân viên thành công!");
    lamMoi();
}

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (evt.getClickCount() == 2 && jTable1.getSelectedRow() != -1) {
            int row = jTable1.getSelectedRow();

            txtMaKHoa.setText(jTable1.getValueAt(row, 0).toString());
            txtTenKhoa.setText(jTable1.getValueAt(row, 1).toString());

            // Chuyển đổi ngày yyyy-MM-dd -> dd-MM-yyyy
            String ngaySql = jTable1.getValueAt(row, 2).toString();
            try {
                java.text.SimpleDateFormat sqlFormat = new java.text.SimpleDateFormat("yyyy-MM-dd");
                java.text.SimpleDateFormat viewFormat = new java.text.SimpleDateFormat("dd-MM-yyyy");
                java.util.Date date = sqlFormat.parse(ngaySql);
                txtNgThLap.setText(viewFormat.format(date));
            } catch (Exception ex) {
                txtNgThLap.setText(ngaySql); // fallback nếu lỗi
            }

            txtTKhoa.setText(jTable1.getValueAt(row, 3).toString());
            txtSDTT.setText(jTable1.getValueAt(row, 4).toString());
            txtEmail.setText(jTable1.getValueAt(row, 5).toString());
            txtDC.setText(jTable1.getValueAt(row, 6).toString());
            txtGhiChu.setText(jTable1.getValueAt(row, 7).toString());
            String trangThai = jTable1.getValueAt(row, 8).toString();
            if ("Hoạt động".equals(trangThai)) {
                jRadioButton1.setSelected(true);
            } else {
                jRadioButton2.setSelected(true);
            }
            jTabbedPane1.setSelectedIndex(1);
            // ẩn 
            txtMaKHoa.setEditable(false);
            btnAdd.setEnabled(false);
            // cập nhật thống kê nhanh
            thongKeNhanh(txtMaKHoa.getText().trim());
            // hện nút linn kết 
            btnLk.setEnabled(true);
        }
    }//GEN-LAST:event_jTable1MouseClicked


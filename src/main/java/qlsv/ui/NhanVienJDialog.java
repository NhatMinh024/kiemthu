private void them() {
    // Kiểm tra thông tin không được rỗng
    if (txtMaNV.getText().trim().isEmpty() ||
        txtMatKhau.getText().trim().isEmpty() ||
        txtHoTen.getText().trim().isEmpty() ||
        txtEmail.getText().trim().isEmpty() ||
        txtSDT.getText().trim().isEmpty() ||
        txtDiaChi.getText().trim().isEmpty() ||
        txtNgayTao.getText().trim().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Không được để trống thông tin!");
        return;
    }
    // Kiểm tra trùng mã nhân viên
    NhanVienDAO dao = new NhanVienDAOImpl();
    if (dao.findById(txtMaNV.getText().trim()) != null) {
        javax.swing.JOptionPane.showMessageDialog(this, "Mã nhân viên đã tồn tại!");
        return;
    }
    // Chuyển đổi ngày tạo từ String sang java.sql.Date (dd/MM/yyyy)
    java.sql.Date ngayTao;
    try {
        java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd-MM-yyyy");
        java.util.Date utilDate = sdf.parse(txtNgayTao.getText().trim());
        ngayTao = new java.sql.Date(utilDate.getTime());
    } catch (Exception ex) {
        javax.swing.JOptionPane.showMessageDialog(this, "Ngày tạo không đúng định dạng (Ngày-Tháng-Năm)!");
        return;
    }

    // Tạo đối tượng nhân viên
    NhanVien nv = new NhanVien();
    nv.setMaNV(txtMaNV.getText().trim());
    nv.setMatKhau(txtMatKhau.getText().trim());
    nv.setHoTen(txtHoTen.getText().trim());
    nv.setEmail(txtEmail.getText().trim());
    nv.setSdt(txtSDT.getText().trim());
    nv.setDiaChi(txtDiaChi.getText().trim());
    nv.setNgayTao(ngayTao);
    nv.setGioiTinh("Nam".equals(cboGioiTinh.getSelectedItem()));
    nv.setVaiTro(rdQuanTri.isSelected());
    nv.setTrangThai(rdHoatDong.isSelected());
    lamMoi();

    if (dao.insert(nv)) {
        fillTableNhanVien();
        javax.swing.JOptionPane.showMessageDialog(this, "Thêm nhân viên thành công!");
        lamMoi();
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Thêm nhân viên thất bại!");
    }
}
private void xoa() {
    int row = tblBangNV.getSelectedRow();
    if (row == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng chọn nhân viên cần xóa!");
        return;
    }
    String trangThai = tblBangNV.getValueAt(row, 9).toString().trim(); // Cột 9 là trạng thái
    if ("Hoạt động".equalsIgnoreCase(trangThai)) {
        javax.swing.JOptionPane.showMessageDialog(this, "Không thể xóa nhân viên đang hoạt động!");
        return;
    }
    int confirm = javax.swing.JOptionPane.showConfirmDialog(
        this,
        "Bạn có chắc chắn muốn xóa nhân viên này?",
        "Xác nhận xóa",
        javax.swing.JOptionPane.YES_NO_OPTION
    );
    if (confirm != javax.swing.JOptionPane.YES_OPTION) {
        return;
    }
    String maNV = tblBangNV.getValueAt(row, 0).toString();
    NhanVienDAO dao = new NhanVienDAOImpl();
    dao.delete(maNV);
    fillTableNhanVien();
    javax.swing.JOptionPane.showMessageDialog(this, "Xóa nhân viên thành công!");
    lamMoi();
}

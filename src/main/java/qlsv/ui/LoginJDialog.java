    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
         String username = txtUserName.getText().trim();// lấy tên đăng nhập 
    String password = new String(txtPass.getPassword());// lấy mật khẩu 

    // Kiểm tra rỗng 
    if (username.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng nhập tên đăng nhập!", "Thông báo", javax.swing.JOptionPane.WARNING_MESSAGE);
        txtUserName.requestFocus();
        return;
    }
    if (password.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng nhập mật khẩu!", "Thông báo", javax.swing.JOptionPane.WARNING_MESSAGE);
        txtPass.requestFocus();
        return;
    }

    // Gọi DAO để xác thực đăng nhập
    qlsv.dao.NhanVienDAO dao = new qlsv.dao.impl.NhanVienDAOImpl();
    qlsv.enity.NhanVien user = dao.login(username, password);

    if (user == null) {
        javax.swing.JOptionPane.showMessageDialog(this, "Sai tên đăng nhập hoặc mật khẩu, hoặc tài khoản đã bị khóa!", "Lỗi đăng nhập", javax.swing.JOptionPane.ERROR_MESSAGE);
        txtPass.setText("");
        txtUserName.requestFocus();
    } else {
    qlsv.util.Auth.user = user;

    javax.swing.JOptionPane.showMessageDialog(
        this,
        "Đăng nhập thành công!\nChào mừng: " + user.getHoTen(),
        "Thành công",
        javax.swing.JOptionPane.INFORMATION_MESSAGE
    );

    // MỞ MÀN HÌNH CHÍNH
    new QlsvJFrame().setVisible(true);

    // ĐÓNG LOGIN
    this.dispose();
}
    }//GEN-LAST:event_btnLoginActionPerformed


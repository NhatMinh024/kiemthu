    private void xoa() {
        String maMH = txtMaMonHoc.getText().trim();
        if (maMH.isEmpty()) {// nếu chưa chọn 
            javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng chọn môn học muốn xóa!");
            return;
        }
        if (isMonHocLienQuan(maMH)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Không thể xóa môn học này vì đã có dữ liệu liên quan!");
            return;
        }
        // gọi hàm hỏi 
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa môn học này?", "Xác nhận xóa", javax.swing.JOptionPane.YES_NO_OPTION);
        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            MonHocDAO dao = new MonHocDAOImpl();
            try {
                dao.delete(maMH);// gọi dao để xóa 
                javax.swing.JOptionPane.showMessageDialog(this, "Xóa môn học thành công!");
                fillTableMonHoc();
                // Làm mới biểu mẫu sau khi xóa
                lamMoi();
            } catch (Exception e) {
                javax.swing.JOptionPane.showMessageDialog(this, "Xóa môn học thất bại!\n" + e.getMessage());
            }
        }
    }

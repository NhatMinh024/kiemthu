    private void xoaLop() {
    String maLop = jTextField1.getText().trim();
    if (maLop.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Không tìm thấy mã lớp để xóa!");
        return;
    }

    // Kiểm tra lớp có sinh viên hoặc dữ liệu liên quan không
    qlsv.dao.SinhVienDAO svDao = new qlsv.dao.impl.SinhVienDAOImpl();
    java.util.List<qlsv.enity.SinhVien> ds = svDao.selectByMaLop(maLop);
    if (ds != null && !ds.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Không thể xóa lớp này vì có sinh viên hoặc dữ liệu liên quan!");
        return;
    }

    int confirm = javax.swing.JOptionPane.showConfirmDialog(this,
            "Bạn có chắc chắn muốn xóa lớp này không?",
            "Xác nhận xóa",
            javax.swing.JOptionPane.YES_NO_OPTION);
    if (confirm != javax.swing.JOptionPane.YES_OPTION) {
        return;
    }

    qlsv.dao.LopDAO dao = new qlsv.dao.impl.LopDAOImpl();
    try {
        dao.delete(maLop);
        javax.swing.JOptionPane.showMessageDialog(this, "Xóa lớp thành công!");
        dispose();
    } catch (Exception ex) {
        javax.swing.JOptionPane.showMessageDialog(this, "Xóa lớp thất bại!\n" + ex.getMessage());
    }
}


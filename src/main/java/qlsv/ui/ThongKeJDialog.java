    // đỏ dữ liệu lên bảng 
     private void loadThongKe() {
        ThongKeDAOImpl dao = new ThongKeDAOImpl();
        List<ThongKe> list = dao.getThongKe();
        DefaultTableModel model = (DefaultTableModel) tblDS.getModel();
        model.setRowCount(0);

        int tongSV = 0, tongNam = 0, tongNu = 0, tongGioi = 0, tongKha = 0, tongTB = 0;

        for (ThongKe tk : list) {
            model.addRow(new Object[]{
                tk.getKhoa(),
                tk.getLop(),
                tk.getSoSinhVien(),
                tk.getNam(),
                tk.getNu(),
                tk.getGioi(),
                tk.getKha(),
                tk.getTrungBinh()
            });
            tongSV += tk.getSoSinhVien();
            tongNam += tk.getNam();
            tongNu += tk.getNu();
            tongGioi += tk.getGioi();
            tongKha += tk.getKha();
            tongTB += tk.getTrungBinh();
        }

        lblTongSV.setText(String.valueOf(tongSV));
        lblTongNam.setText(String.valueOf(tongNam));
        lblTongNu.setText(String.valueOf(tongNu));
        lblTongGioi.setText(String.valueOf(tongGioi));
        lblTongKha.setText(String.valueOf(tongKha));
        lblTongTB.setText(String.valueOf(tongTB));
    }
    // xuất excel
    private void xuatExcel() {
    try {
        javax.swing.JFileChooser chooser = new javax.swing.JFileChooser();
        chooser.setDialogTitle("Chọn nơi lưu file Excel");// hiển thị nới lưu file 
        int userSelection = chooser.showSaveDialog(this);
        if (userSelection != javax.swing.JFileChooser.APPROVE_OPTION) return;

        String filePath = chooser.getSelectedFile().getAbsolutePath();
        if (!filePath.endsWith(".xlsx")) filePath += ".xlsx";

        org.apache.poi.ss.usermodel.Workbook workbook = new org.apache.poi.xssf.usermodel.XSSFWorkbook();
        org.apache.poi.ss.usermodel.Sheet sheet = workbook.createSheet("ThongKeSinhVien");
        DefaultTableModel model = (DefaultTableModel) tblDS.getModel();// tạo cái file excel mớteeni tên là thống ke 

        // ghi dòng tiêu đề 
        org.apache.poi.ss.usermodel.Row headerRow = sheet.createRow(0);
        for (int i = 0; i < model.getColumnCount(); i++) {
            headerRow.createCell(i).setCellValue(model.getColumnName(i));
        }

        // ghi dữ liệu cho người dùng 
        for (int i = 0; i < model.getRowCount(); i++) {
            org.apache.poi.ss.usermodel.Row row = sheet.createRow(i + 1);
            for (int j = 0; j < model.getColumnCount(); j++) {
                Object value = model.getValueAt(i, j);
                row.createCell(j).setCellValue(value != null ? value.toString() : "");
            }
        }

        // Tổng hợp
        int lastRow = model.getRowCount() + 2;
        sheet.createRow(lastRow).createCell(0).setCellValue("Tổng số sinh viên:");
        sheet.getRow(lastRow).createCell(1).setCellValue(lblTongSV.getText());
        sheet.getRow(lastRow).createCell(2).setCellValue("Nam:");
        sheet.getRow(lastRow).createCell(3).setCellValue(lblTongNam.getText());
        sheet.getRow(lastRow).createCell(4).setCellValue("Nữ:");
        sheet.getRow(lastRow).createCell(5).setCellValue(lblTongNu.getText());
        sheet.getRow(lastRow).createCell(6).setCellValue("Giỏi:");
        sheet.getRow(lastRow).createCell(7).setCellValue(lblTongGioi.getText());
        sheet.getRow(lastRow).createCell(8).setCellValue("Khá:");
        sheet.getRow(lastRow).createCell(9).setCellValue(lblTongKha.getText());
        sheet.getRow(lastRow).createCell(10).setCellValue("Trung bình:");
        sheet.getRow(lastRow).createCell(11).setCellValue(lblTongTB.getText());

        try (java.io.FileOutputStream fos = new java.io.FileOutputStream(filePath)) {
            workbook.write(fos);
            workbook.close();
        }
        javax.swing.JOptionPane.showMessageDialog(this, "Xuất Excel thành công!");
    } catch (Exception ex) {
        javax.swing.JOptionPane.showMessageDialog(this, "Lỗi xuất Excel: " + ex.getMessage());
    }
} 

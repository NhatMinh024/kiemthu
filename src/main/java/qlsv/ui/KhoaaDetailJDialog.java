    private void themSinhVienVaoLop() {
    // Lấy danh sách lớp trong khoa
    qlsv.dao.LopDAO lopDao = new qlsv.dao.impl.LopDAOImpl();
    List<Lop> danhSachLop = lopDao.selectByMaKhoa(maKhoa);
    
    if (danhSachLop.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Khoa này chưa có lớp nào!\n\n" +
            "Vui lòng tạo lớp trước khi thêm sinh viên.",
            "Thông báo",
            javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    // Tạo danh sách tên lớp để hiển thị
    String[] tenCacLop = new String[danhSachLop.size()];
    for (int i = 0; i < danhSachLop.size(); i++) {
        Lop lop = danhSachLop.get(i);
        tenCacLop[i] = lop.getMaLop() + " - " + lop.getTenLop();
    }
    
    // Hiển thị dialog chọn lớp
    String lopDuocChon = (String) javax.swing.JOptionPane.showInputDialog(
        this,
        "Bạn muốn thêm sinh viên vào lớp nào?\n\n" +
        "Khoa: " + tenKhoa + "\n" +
        " Vui lòng chọn lớp:",
        "Chọn lớp để thêm sinh viên",
        javax.swing.JOptionPane.QUESTION_MESSAGE,
        null,
        tenCacLop,
        tenCacLop[0] // Mặc định chọn lớp đầu tiên
    );
    
    // Nếu user không chọn lớp (Cancel)
    if (lopDuocChon == null) {
        return;
    }
    
    // Lấy mã lớp từ string được chọn (tách phần đầu trước dấu " - ")
    String maLopDuocChon = lopDuocChon.split(" - ")[0];
    
    // Tìm đối tượng Lop tương ứng
    Lop lopCanThem = null;
    for (Lop lop : danhSachLop) {
        if (lop.getMaLop().equals(maLopDuocChon)) {
            lopCanThem = lop;
            break;
        }
    }
    
    if (lopCanThem == null) {
        javax.swing.JOptionPane.showMessageDialog(this,
            " Lỗi: Không tìm thấy lớp được chọn!",
            "Lỗi",
            javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    // Mở form danh sách sinh viên chưa có lớp (thay tên form của bạn vào đây)
    // Ví dụ: SinhVienChuaCoLopJDialog hoặc tên form bạn đã tạo
    try {
        // THAY ĐỔI TÊN CLASS NÀY THEO FORM CỦA BẠN
        ThemSVJDialog dialog = new ThemSVJDialog(
            (java.awt.Frame) this.getParent(),
            true,
            maLopDuocChon          // Truyền mã lớp được chọn
            
        ); 
        dialog.setLocationRelativeTo(this);
        dialog.setVisible(true); 
        
        // Sau khi đóng form thêm sinh viên, reload lại bảng
        loadTableSinhVien();
        
    } catch (Exception ex) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Lỗi mở form thêm sinh viên!\n\n" +
            " Chi tiết: " + ex.getMessage(),
            "Lỗi",
            javax.swing.JOptionPane.ERROR_MESSAGE);
        ex.printStackTrace();
    }
}

    private void Luu() {
        if (txtMaMon.getText().trim().isEmpty() || txtMaMon.isEnabled()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng chọn lớp trước!");
            return;
        }

        // Lấy dữ liệu từ form
        String maMon = txtMaMon.getText().trim();
        String tenMon = txtTenMon.getText().trim();
        String soTinChiStr = txtSoTinChi.getText().trim();
        String maGV = txtMaGV.getText().trim();
        String tenKhoa = txtTenKhoa.getText().trim();
        String maHK = txtMaHocKy.getText().trim();

        // Kiểm tra trống
        if (tenMon.isEmpty() || soTinChiStr.isEmpty() || maGV.isEmpty() || tenKhoa.isEmpty() || maHK.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Không được để trống bất kỳ thông tin nào!");
            return;
        }

        // Kiểm tra số tín chỉ
        int soTinChi;
        try {
            soTinChi = Integer.parseInt(soTinChiStr);
            if (soTinChi < 1 || soTinChi > 4) {
                javax.swing.JOptionPane.showMessageDialog(this, "Số tín chỉ phải là số nguyên từ 1 đến 4!");
                return;
            }
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Số tín chỉ phải là số nguyên!");
            return;
        }

        // Kiểm tra mã giáo viên tồn tại bằng SQL
        String sqlGV = "SELECT COUNT(*) FROM NhanVien WHERE MaNV = ?";
        try (java.sql.ResultSet rs = qlsv.util.XJDBC.executeQuery(sqlGV, maGV)) {
            if (rs.next() && rs.getInt(1) == 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Mã giáo viên không tồn tại!");
                return;
            }
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Lỗi kiểm tra mã giáo viên!");
            return;
        }

        // Kiểm tra tên khoa tồn tại bằng SQL và lấy mã khoa
        String maKhoa = "";
        String sqlKhoa = "SELECT MaKhoa FROM Khoa WHERE MaKhoa = ?";
        try (java.sql.ResultSet rs = qlsv.util.XJDBC.executeQuery(sqlKhoa, tenKhoa)) {
            if (rs.next()) {
                maKhoa = rs.getString("MaKhoa");
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Mã khoa không tồn tại!");
                return;
            }
        } catch (SQLException ex) {
            Logger.getLogger(HocKyJDialog.class.getName()).log(Level.SEVERE, null, ex);
        }

        // Nếu hợp lệ, lưu vào CSDL
        try {
            MonHoc mh = new MonHoc(maMon, tenMon, soTinChi, maGV, maKhoa, maHK);
            monHocDAO.update(mh);
            javax.swing.JOptionPane.showMessageDialog(this, "Lưu thông tin môn học thành công!");
            hienThiMonHocTheoHocKy();
            lamMoiMH();
            // Làm mới bảng hoặc form nếu cần
        } catch (Exception ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Lỗi lưu môn học: " + ex.getMessage());
        }
    }


private void xoaCacMucDaChon() {
    javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblBangNV.getModel();
    NhanVienDAO dao = new NhanVienDAOImpl();
    // duyệt từ cuối lên đầu để tránh xóa nhầm hàng 
    for (int i = model.getRowCount() - 1; i >= 0; i--) {
        Boolean checked = (Boolean) model.getValueAt(i, model.getColumnCount() - 1);
        // nếu check box ở cuối là true thì thì thực hiện xóa 
        if (checked != null && checked) {
            String maNV = (String) model.getValueAt(i, 0); // Cột 0 là mã nhân viên
            String vaiTro = (String) model.getValueAt(i, 7); // Cột 7 là vai trò
            if ("Quản trị".equalsIgnoreCase(vaiTro) || "Quản trị viên".equalsIgnoreCase(vaiTro)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Không thể xóa tài khoản quản trị: " + maNV);
                continue;
            }
            dao.delete(maNV);// gọi dao để xóa khỏi database 
            model.removeRow(i);// xóa dòng khỏi giaodienj 
        }
    }
}

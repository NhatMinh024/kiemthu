// Chuyển ngày sinh từ yyyy-MM-dd sang dd/MM/yyyy khi set lên form

    public void setSinhVien(qlsv.enity.SinhVien sv) {
        // hiển thị mã số sinh viên và họ tên 
        txtMsoSV.setText(sv.getMaSV());
        txtMsoSV.setEditable(false);
        txtHoTen.setText(sv.getHoTen());

        // Chuyển ngày sinh từ database sang định dạng hiển thị 
        String ngaySinhSql = sv.getNgaySinh(); // ví dụ: "2000-01-01"
        String ngaySinhForm = ngaySinhSql;
        try {
            java.text.SimpleDateFormat sdfSql = new java.text.SimpleDateFormat("yyyy-MM-dd");
            java.text.SimpleDateFormat sdfForm = new java.text.SimpleDateFormat("dd/MM/yyyy");
            java.util.Date date = sdfSql.parse(ngaySinhSql);
            ngaySinhForm = sdfForm.format(date);
        } catch (Exception e) {
            // Nếu lỗi thì giữ nguyên giá trị
        }
        txtNgaySinh.setText(ngaySinhForm);
        // đặt các thông tin vào các ô nhập tương ứng 
        txtDiaChi.setText(sv.getDiaChi());
        txtSDT.setText(sv.getSdt());
        txtEmail.setText(sv.getEmail());
        txtMaLop.setText(sv.getMaLop());
        txtMaLop.setEditable(false);

        // Giới tính và trạng thái như cũ
        //0 là nữ 1 là nam 
        String gt = sv.getGioiTinh();
        if ("0".equals(gt) || "Nữ".equalsIgnoreCase(gt) || "false".equalsIgnoreCase(gt)) {
            cboGioiTinh.setSelectedItem("Nữ");
        } else {
            cboGioiTinh.setSelectedItem("Nam");
        }
        rdoDangHoc.setSelected("0".equals(sv.getTrangThai()));
        rdoNghiHoc.setSelected("1".equals(sv.getTrangThai()));
        rdoTotNghiep.setSelected("2".equals(sv.getTrangThai()));
    }


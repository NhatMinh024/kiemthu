    private void tblSinhVienMouseClicked(java.awt.event.MouseEvent evt) {
        int row = tblSinhVien.getSelectedRow();
        if (evt.getClickCount() == 2 && row >= 0) {
            // Hiện nút Sửa, Xóa ẩn thêm 
            btnThem.setEnabled(false);
            btnSua.setEnabled(true);
            btnXoa.setEnabled(true);

            // Chuyển sang tab Biểu mẫu
            jTabbedPane1.setSelectedIndex(1);

            // Hiển thị dữ liệu lên form
            showDetail(row);
        }
    }

    private void showDetail(int row) {
        DefaultTableModel model = (DefaultTableModel) tblSinhVien.getModel();
        System.out.println("==== Dữ liệu dòng " + row + " ====");
        for (int i = 0; i < model.getColumnCount(); i++) {
            System.out.println("Cột " + i + ": " + model.getValueAt(row, i));
        }
        txtMaSv.setText(getString(model.getValueAt(row, 0)));
        txtHoTen.setText(getString(model.getValueAt(row, 1)));
        txtNgSinh.setText(getString(model.getValueAt(row, 2)));
        txtDiaChi.setText(getString(model.getValueAt(row, 4)));
        txtSDT.setText(getString(model.getValueAt(row, 5)));
        txtEmail.setText(getString(model.getValueAt(row, 6)));
        txtMaLop.setText(getString(model.getValueAt(row, 7)));
    
        // Đổ dữ liệu cho ComboBox giới tính
        String gioiTinh = getString(model.getValueAt(row, 3)).trim();
        if (gioiTinh.equalsIgnoreCase("Nam")) {
            cboGioiTinh.setSelectedItem("Nam");
        } else {
            cboGioiTinh.setSelectedItem("Nữ");
        }
    
        // Đổ dữ liệu cho ComboBox trạng thái
        String trangThai = getString(model.getValueAt(row, 8)).trim();
        if (trangThai.equalsIgnoreCase("Đang học")) {
            cboTrangThai.setSelectedItem("Đang học");
        } else if (trangThai.equalsIgnoreCase("Nghỉ học")) {
            cboTrangThai.setSelectedItem("Nghĩ học");
        } else if (trangThai.equalsIgnoreCase("Tốt nghiệp")) {
            cboTrangThai.setSelectedItem("Tốt nghiệp");
        } else {
            cboTrangThai.setSelectedIndex(0);
        }
    
        txtMaSv.setEditable(false);
    }
    

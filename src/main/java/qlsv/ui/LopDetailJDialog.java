   private void xoaSinhVien() {
    int row = jTable1.getSelectedRow();
    if (row == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng chọn sinh viên để xóa!");
        return;
    }
    // Lấy mã sinh viên và trạng thái từ bảng
    javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
    String maSV = model.getValueAt(row, 0).toString();
    String trangThai = model.getValueAt(row, 8).toString().trim(); // Cột 8 là trạng thái

    if ("Đang học".equalsIgnoreCase(trangThai)) {
        javax.swing.JOptionPane.showMessageDialog(this, "Không thể xóa sinh viên có trạng thái 'Đang học'!");
        return;
    }

    int confirm = javax.swing.JOptionPane.showConfirmDialog(this,
            "Bạn có chắc chắn muốn xóa sinh viên này không?",
            "Xác nhận xóa",
            javax.swing.JOptionPane.YES_NO_OPTION);

    if (confirm != javax.swing.JOptionPane.YES_OPTION) {
        return;
    }

    qlsv.dao.SinhVienDAO dao = new qlsv.dao.impl.SinhVienDAOImpl();
    try {
        dao.delete(maSV);
        javax.swing.JOptionPane.showMessageDialog(this, "Xóa sinh viên thành công!");
        loadSinhVienTheoLop(jTextField1.getText().trim());
    } catch (Exception ex) {
        javax.swing.JOptionPane.showMessageDialog(this, "Xóa sinh viên thất bại!\n" + ex.getMessage());
    }
}

    // chuyển lớp 
    private void chuyenLopSinhVien() {
        int row = jTable1.getSelectedRow();
        if (row == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng chọn sinh viên để chuyển lớp!");
            return;
        }
        // Lấy mã sinh viên từ bảng
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        String maSV = model.getValueAt(row, 0).toString();

        // Nhập mã lớp mới
        String maLopMoi = javax.swing.JOptionPane.showInputDialog(this, "Nhập mã lớp cần chuyển:");
        if (maLopMoi == null || maLopMoi.trim().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Bạn chưa nhập mã lớp mới!");
            return;
        }

        // Kiểm tra mã lớp mới có tồn tại không
        qlsv.dao.LopDAO lopDao = new qlsv.dao.impl.LopDAOImpl();
        boolean tonTaiLopMoi = false;
        for (qlsv.enity.Lop lop : lopDao.selectAll()) {
            if (lop.getMaLop().equalsIgnoreCase(maLopMoi.trim())) {
                tonTaiLopMoi = true;
                break;
            }
        }
        if (!tonTaiLopMoi) {
            javax.swing.JOptionPane.showMessageDialog(this, "Mã lớp mới không tồn tại!");
            return;
        }

        // Thực hiện chuyển lớp
        qlsv.dao.SinhVienDAO svDao = new qlsv.dao.impl.SinhVienDAOImpl();
        java.util.List<qlsv.enity.SinhVien> ds = svDao.selectByMaSV(maSV);
        if (ds != null && !ds.isEmpty()) {
            qlsv.enity.SinhVien sv = ds.get(0);
            sv.setMaLop(maLopMoi.trim());
            try {
                svDao.update(sv);
                javax.swing.JOptionPane.showMessageDialog(this, "Chuyển lớp thành công!");
                loadSinhVienTheoLop(jTextField1.getText().trim());
            } catch (Exception ex) {
                javax.swing.JOptionPane.showMessageDialog(this, "Chuyển lớp thất bại!\n" + ex.getMessage());
            }
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Không tìm thấy sinh viên để chuyển lớp!");
        }
    }

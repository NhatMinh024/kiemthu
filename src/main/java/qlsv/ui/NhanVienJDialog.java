    private void tblBangNVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBangNVMouseClicked
        // TODO add your handling code here:
       int row = tblBangNV.getSelectedRow();
    if (evt.getClickCount() == 2 && row != -1) {
        Object maNV = tblBangNV.getValueAt(row, 0);
        Object matKhau = tblBangNV.getValueAt(row, 1);
        Object hoTen = tblBangNV.getValueAt(row, 2);
        Object email = tblBangNV.getValueAt(row, 3);
        Object sdt = tblBangNV.getValueAt(row, 4);
        Object gioiTinh = tblBangNV.getValueAt(row, 5);
        Object diaChi = tblBangNV.getValueAt(row, 6);
        Object vaiTro = tblBangNV.getValueAt(row, 7);
        Object ngayTao = tblBangNV.getValueAt(row, 8);
        Object trangThai = tblBangNV.getValueAt(row, 9);

        txtMaNV.setText(maNV != null ? maNV.toString() : "");
        txtMatKhau.setText(matKhau != null ? matKhau.toString() : "");
        txtHoTen.setText(hoTen != null ? hoTen.toString() : "");
        txtEmail.setText(email != null ? email.toString() : "");
        txtSDT.setText(sdt != null ? sdt.toString() : "");
        cboGioiTinh.setSelectedItem(gioiTinh != null ? gioiTinh.toString() : "");
        txtDiaChi.setText(diaChi != null ? diaChi.toString() : "");
        if (vaiTro != null && "Quản trị".equalsIgnoreCase(vaiTro.toString())) {
            rdQuanTri.setSelected(true);
        } else {
            rdGiaoVien.setSelected(true);
        }
        txtNgayTao.setText(ngayTao != null ? ngayTao.toString() : "");
        if (trangThai != null && "Hoạt động".equalsIgnoreCase(trangThai.toString())) {
            rdHoatDong.setSelected(true);
        } else {
            rdNgungHD.setSelected(true);
        }
        jTabbedPane1.setSelectedIndex(1); // Chuyển sang tab Biểu mẫu
        txtMaNV.setEnabled(false);
        txtMaNV.setEditable(false);
    }
    }//GEN-LAST:event_tblBangNVMouseClicked


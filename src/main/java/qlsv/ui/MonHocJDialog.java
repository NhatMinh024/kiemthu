    private boolean isMonHocLienQuan(String maMH) {
        // Kiểm tra trong bảng ThoiKhoaBieu
        String sql1 = "SELECT COUNT(*) FROM ThoiKhoaBieu WHERE MaMH = ?";
        // Kiểm tra trong bảng Diem
        String sql2 = "SELECT COUNT(*) FROM Diem WHERE MaMH = ?";
        try (java.sql.Connection con = qlsv.util.XJDBC.getConnection()) {
            try (java.sql.PreparedStatement ps1 = con.prepareStatement(sql1)) {
                ps1.setString(1, maMH);
                java.sql.ResultSet rs1 = ps1.executeQuery();
                rs1.next();
                if (rs1.getInt(1) > 0) {
                    return true;
                }
            }
            try (java.sql.PreparedStatement ps2 = con.prepareStatement(sql2)) {
                ps2.setString(1, maMH);
                java.sql.ResultSet rs2 = ps2.executeQuery();
                rs2.next();
                if (rs2.getInt(1) > 0) {
                    return true;
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return false;
    }

    private void xoaCacMucDaChon() {
        // lấy model của bảng và tạo dao 
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblMonHoc.getModel();
        MonHocDAO dao = new MonHocDAOImpl();
        boolean daChon = false;
        // nếu chọn là true thì cho phép xóa ko thì dừng lại luôn 
        for (int i = model.getRowCount() - 1; i >= 0; i--) {
            Boolean checked = (Boolean) model.getValueAt(i, model.getColumnCount() - 1);
            if (checked != null && checked) {
                daChon = true;
                break;
            }
        }
        if (!daChon) {
            javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng chọn môn học để xóa!");
            return;
        }

        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa các mục đã chọn?", "Xác nhận xóa", javax.swing.JOptionPane.YES_NO_OPTION);
        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            boolean daXoa = false;
            // duyệt ngc để tránh xóa nhiều dòng 
            for (int i = model.getRowCount() - 1; i >= 0; i--) {
                Boolean checked = (Boolean) model.getValueAt(i, model.getColumnCount() - 1);
                if (checked != null && checked) {
                    String maMH = (String) model.getValueAt(i, 0); // cột 0 là mã môn học
                    if (isMonHocLienQuan(maMH)) {
                        javax.swing.JOptionPane.showMessageDialog(this, "Không thể xóa môn học " + maMH + " vì đã có dữ liệu liên quan!");
                        continue; // bỏ qua không xóa
                    }
                    dao.delete(maMH); // Xóa trên database
                    model.removeRow(i); // Xóa trên giao diện
                    daXoa = true;
                }
            }
            if (daXoa) {
                javax.swing.JOptionPane.showMessageDialog(this, "Xóa thành công các môn học không có ràng buộc!");
            }
        }
    }

    private boolean isMaGVExists(String maGV) {
        // ntaoj đối tượng dao cho manv 
        qlsv.dao.NhanVienDAO nvDao = new qlsv.dao.impl.NhanVienDAOImpl();
        return nvDao.findById(maGV) != null;// tìm thấy về true còn null thì false 
    }

    private void them() {
        String maMH = txtMaMonHoc.getText().trim();
        String tenMH = txtTenMonHoc.getText().trim();
        String soTinChiStr = txtSoTinChi.getText().trim();
        String hocKyStr = cboHocKy.getSelectedItem().toString().replace("Kỳ ", "");
        String maGV = txtMaGiaoVien.getText().trim();// lấy dữ liệu từ ô nhập 

        // Kiểm tra nhập đầy đủ
        if (maMH.isEmpty() || tenMH.isEmpty() || soTinChiStr.isEmpty() || hocKyStr.isEmpty() || maGV.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!");
            return;
        }

        // Kiểm tra số tín chỉ là số nguyên dương
        int soTinChi;
        try {
            soTinChi = Integer.parseInt(soTinChiStr);
            if (soTinChi <= 0) {
                throw new NumberFormatException();
            }
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Số tín chỉ phải là số nguyên dương!");
            return;
        }

        // Kiểm tra mã môn học trùng
        MonHocDAO dao = new MonHocDAOImpl();
        for (MonHoc mh : dao.selectAll()) {
            if (mh.getMaMH().equalsIgnoreCase(maMH)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Mã môn học đã tồn tại!");
                return;
            }
        }

        // Kiểm tra mã giáo viên có tồn tại
        if (!isMaGVExists(maGV)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Mã giáo viên này không tồn tại!");
            return;
        }

        // Thêm môn học mới
        MonHoc monHoc = new MonHoc();
        monHoc.setMaMH(maMH);
        monHoc.setTenMH(tenMH);
        monHoc.setSoTinChi(soTinChi);
        monHoc.setMaHK(hocKyStr);
        monHoc.setMaGV(maGV);

        dao.insert(monHoc);
        javax.swing.JOptionPane.showMessageDialog(this, "Thêm môn học thành công!");
        fillTableMonHoc();
        lamMoi();
        btnThem.setVisible(true);
        txtMaMonHoc.setEditable(true);
    }

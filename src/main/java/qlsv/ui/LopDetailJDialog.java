    private void loadSinhVienTheoLop(String maLop) {
        qlsv.dao.SinhVienDAO dao = new qlsv.dao.impl.SinhVienDAOImpl();
        java.util.List<qlsv.enity.SinhVien> list = dao.selectByMaLop(maLop);
        // lấy danh sách sinh viên theo mã lớp 

        // Xóa dữ liệu cũ trên bảng
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        // Thống kê
        int tong = 0, nam = 0, nu = 0;

        for (qlsv.enity.SinhVien sv : list) {
            // Chuyển đổi giới tính
            String gioiTinhStr = "Nam";
            if (sv.getGioiTinh().equals("0") || sv.getGioiTinh().equalsIgnoreCase("false") || sv.getGioiTinh().equalsIgnoreCase("nu") || sv.getGioiTinh().equalsIgnoreCase("nữ")) {
                gioiTinhStr = "Nữ";
                nu++;
            } else {
                nam++;
            }
            // Chuyển đổi trạng thái
            String trangThaiStr = "Đang học";
            if ("1".equals(sv.getTrangThai())) {
                trangThaiStr = "Nghỉ học";
            } else if ("2".equals(sv.getTrangThai())) {
                trangThaiStr = "Tốt nghiệp";
            }
            model.addRow(new Object[]{
                sv.getMaSV(),
                sv.getHoTen(),
                sv.getNgaySinh(),
                gioiTinhStr,
                sv.getDiaChi(),
                sv.getSdt(),
                sv.getEmail(),
                sv.getMaLop(),
                trangThaiStr,
                false
            });
            tong++;
        }

        // Hiển thị lên các ô thống kê nhanh
        txtTong.setText(String.valueOf(tong));
        txtNam.setText(String.valueOf(nam));
        txtNu.setText(String.valueOf(nu));
    }

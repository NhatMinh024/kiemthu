    private void fillTableSinhVien() {
        DefaultTableModel model = (DefaultTableModel) tblSinhVien.getModel();
        model.setRowCount(0); // Xóa dữ liệu cũ
        // lấy danh sách sinh viên từ dtb 
        SinhVienDAO dao = new SinhVienDAOImpl();
        List<SinhVien> list = dao.selectAll();
        // tạo đối tượng dao và lấy toàn bộ sinh viên từ dtb 
        for (SinhVien sv : list) {
            // Chuyển trạng thái số sang chữ
            String trangThaiText;
            switch (sv.getTrangThai()) {
                case "0":
                    trangThaiText = "Đang học";
                    break;
                case "1":
                    trangThaiText = "Nghỉ học";
                    break;
                case "2":
                    trangThaiText = "Tốt nghiệp";
                    break;
                default:
                    trangThaiText = "Không xác định";
            }
            // Chuyển giới tính số sang chữ
            String gioiTinhText = "0".equals(sv.getGioiTinh()) ? "Nữ" : "Nam";

            model.addRow(new Object[]{
                sv.getMaSV(),
                sv.getHoTen(),
                sv.getNgaySinh(),
                gioiTinhText, // Hiển thị chữ thay vì số
                sv.getDiaChi(),
                sv.getSdt(),
                sv.getEmail(),
                sv.getMaLop(),
                trangThaiText,
                false // Cột checkbox, mặc định là false
            });
        }
    }

    private void checkAll() {
        // duyệt qua tất cả các dòng trên bảng 
        for (int i = 0; i < tblSinhVien.getRowCount(); i++) {
            tblSinhVien.setValueAt(true, i, 9); // cột 9 là cột checkbox
        }
    }

    private void boChonTatCa() {
        for (int i = 0; i < tblSinhVien.getRowCount(); i++) {
            tblSinhVien.setValueAt(false, i, 9); // cột 9 là cột checkbox
        }
    }

    private void xoaDaChon() {
    DefaultTableModel model = (DefaultTableModel) tblSinhVien.getModel();
    SinhVienDAO dao = new SinhVienDAOImpl();
    boolean hasBlocked = false;
    // Duyệt ngược để tránh lỗi khi xóa dòng
    for (int i = model.getRowCount() - 1; i >= 0; i--) {
        Boolean checked = (Boolean) model.getValueAt(i, 9); // cột 9 là checkbox
        String trangThai = model.getValueAt(i, 8).toString().trim(); // cột 8 là trạng thái
        if (checked != null && checked) {
            if ("Đang học".equalsIgnoreCase(trangThai)) {
                hasBlocked = true;// đánh dấu ko dc xóa 
                continue; // Không xóa sinh viên đang học
            }
            String maSV = model.getValueAt(i, 0).toString();
            dao.delete(maSV); // Xóa trên database
            model.removeRow(i); // Xóa trên bảng giao diện
        }
    }
    fillTableSinhVien(); // Load lại bảng từ database
    // thông báo nếu sinh viên bị chặn xóa 
    if (hasBlocked) {
        javax.swing.JOptionPane.showMessageDialog(this, 
            "Không thể xóa sinh viên có trạng thái 'Đang học'!");
    }
}
    

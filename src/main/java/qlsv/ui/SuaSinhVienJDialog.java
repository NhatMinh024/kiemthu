// SỬA  sinh vien ở form 
private void suaSV() {
    // lấy thông tin từ ô nhập chuyển dữ liệu từ nam nữ sang 0 và 1 
        String maSV = txtMsoSV.getText().trim();
        String hoTen = txtHoTen.getText().trim();
        String ngaySinhForm = txtNgaySinh.getText().trim();
        String gioiTinh = cboGioiTinh.getSelectedItem() != null ? cboGioiTinh.getSelectedItem().toString() : "";
        String gioiTinhDB = "1";
        if ("Nữ".equalsIgnoreCase(gioiTinh)) {
            gioiTinhDB = "0";
        }
        String diaChi = txtDiaChi.getText().trim();
        String sdt = txtSDT.getText().trim();
        String email = txtEmail.getText().trim();
        String maLop = txtMaLop.getText().trim();

        // Kiểm tra trạng thái
        String trangThai = null;
        if (rdoDangHoc.isSelected()) {
            trangThai = "0";
        } else if (rdoNghiHoc.isSelected()) {
            trangThai = "1";
        } else if (rdoTotNghiep.isSelected()) {
            trangThai = "2";
        }

        // Kiểm tra các trường bắt buộc
        if (maSV.isEmpty() || hoTen.isEmpty() || ngaySinhForm.isEmpty() || gioiTinh.isEmpty()
                || diaChi.isEmpty() || sdt.isEmpty() || email.isEmpty() || maLop.isEmpty() || trangThai == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!");
            return;
        }

        // Kiểm tra ngày sinh đúng định dạng dd/MM/yyyy
        if (!ngaySinhForm.matches("\\d{2}/\\d{2}/\\d{4}")) {
            javax.swing.JOptionPane.showMessageDialog(this, "Ngày sinh phải đúng định dạng Ngày/Tháng/Năm (ví dụ: 01/01/2000)!");
            return;
        }

        // Chuyển ngày sinh về yyyy-MM-dd để lưu xuống DB
        String ngaySinhSql = ngaySinhForm;
        try {
            java.text.SimpleDateFormat sdfForm = new java.text.SimpleDateFormat("dd/MM/yyyy");
            java.text.SimpleDateFormat sdfSql = new java.text.SimpleDateFormat("yyyy-MM-dd");
            java.util.Date date = sdfForm.parse(ngaySinhForm);
            ngaySinhSql = sdfSql.format(date);
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Ngày sinh không hợp lệ!");
            return;
        }

        // Kiểm tra số điện thoại đủ 10 số
        if (!sdt.matches("\\d{10}")) {
            javax.swing.JOptionPane.showMessageDialog(this, "Số điện thoại phải đủ 10 số!");
            return;
        }

        // Kiểm tra email hợp lệ
        if (!email.matches("^[\\w\\.-]+@[\\w\\.-]+\\.\\w{2,}$")) {
            javax.swing.JOptionPane.showMessageDialog(this, "Email không hợp lệ!");
            return;
        }

        // Tạo đối tượng SinhVien
        qlsv.enity.SinhVien sv = new qlsv.enity.SinhVien(
                maSV, hoTen, ngaySinhSql, gioiTinhDB, diaChi, sdt, email, maLop, trangThai
        );

        // Sửa vào DB
        qlsv.dao.SinhVienDAO dao = new qlsv.dao.impl.SinhVienDAOImpl();
        try {
            dao.update(sv);
            javax.swing.JOptionPane.showMessageDialog(this, "Cập nhật sinh viên thành công!");
            dispose();
        } catch (Exception ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Cập nhật sinh viên thất bại!\n" + ex.getMessage());
        }
    }

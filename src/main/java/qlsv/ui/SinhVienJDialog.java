    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {
        String maSV = txtTimMaSv.getText().trim();
        DefaultTableModel model = (DefaultTableModel) tblSinhVien.getModel();
        model.setRowCount(0);
        SinhVienDAO dao = new SinhVienDAOImpl();
        List<SinhVien> list;
        if (maSV.isEmpty()) {
            list = dao.selectAll();
        } else {
            list = dao.selectByMaSV(maSV);
        }
        if (list.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Không tìm thấy sinh viên!");
            return;
        }
        for (SinhVien sv : list) {
            String trangThaiText;
            switch (sv.getTrangThai()) {
                case "0":
                    trangThaiText = "Đang học";
                    break;
                case "1":
                    trangThaiText = "Nghỉ học";
                    break;
                case "2":
                    trangThaiText = "Tốt nghiệp";
                    break;
                default:
                    trangThaiText = "Không xác định";
            }
            String gioiTinhText = "0".equals(sv.getGioiTinh()) ? "Nữ" : "Nam";
            model.addRow(new Object[]{
                sv.getMaSV(),
                sv.getHoTen(),
                sv.getNgaySinh(),
                gioiTinhText,
                sv.getDiaChi(),
                sv.getSdt(),
                sv.getEmail(),
                sv.getMaLop(),
                trangThaiText,
                false
            });
        }
    }


    private void themLop() {
        String maLop = jTextField1.getText().trim();
        String tenLop = jTextField2.getText().trim();
        String maGV = jTextField3.getText().trim();
        String nienKhoa = jTextField4.getText().trim();
        String ngayBatDauStr = jTextField5.getText().trim();
        String ngayKetThucStr = jTextField6.getText().trim();
        String siSoStr = jTextField7.getText().trim();
        String maKhoa = cboKhoa.getSelectedItem() != null ? cboKhoa.getSelectedItem().toString().trim() : "";

        // Kiểm tra các trường bắt buộc
        if (maLop.isEmpty() || tenLop.isEmpty() || maGV.isEmpty() || nienKhoa.isEmpty() || ngayBatDauStr.isEmpty() || ngayKetThucStr.isEmpty() || maKhoa.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin bắt buộc!");
            return;
        }

        // Kiểm tra mã giáo viên có tồn tại
        qlsv.dao.NhanVienDAO nvDao = new qlsv.dao.impl.NhanVienDAOImpl();
        boolean tonTaiGV = false;
        for (qlsv.enity.NhanVien nv : nvDao.selectAll()) {
            if (nv.getMaNV().equalsIgnoreCase(maGV)) {
                tonTaiGV = true;
                break;
            }
        }
        if (!tonTaiGV) {
            javax.swing.JOptionPane.showMessageDialog(this, "Mã giáo viên này không tồn tại!");
            return;
        }

        // Kiểm tra định dạng ngày
        if (!ngayBatDauStr.matches("\\d{2}/\\d{2}/\\d{4}") || !ngayKetThucStr.matches("\\d{2}/\\d{2}/\\d{4}")) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Ngày bắt đầu và ngày kết thúc phải đúng định dạng dd/MM/yyyy (ví dụ: 01/01/2023)!");
            return;
        }
        java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd/MM/yyyy");
        sdf.setLenient(false);
        java.util.Date ngayBatDau, ngayKetThuc;
        try {
            ngayBatDau = sdf.parse(ngayBatDauStr);
            ngayKetThuc = sdf.parse(ngayKetThucStr);
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Ngày bắt đầu và ngày kết thúc phải đúng định dạng dd/MM/yyyy!");
            return;
        }

        // Kiểm tra trùng mã lớp
        qlsv.dao.LopDAO dao = new qlsv.dao.impl.LopDAOImpl();
        java.util.List<qlsv.enity.Lop> list = dao.selectAll();
        for (qlsv.enity.Lop lop : list) {
            if (lop.getMaLop().equalsIgnoreCase(maLop)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Mã lớp đã tồn tại!");
                return;
            }
        }

        // Parse sĩ số
        int siSo = 0;
        try {
            siSo = Integer.parseInt(siSoStr);
            if (siSo <= 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Sĩ số phải là số nguyên dương!");
                return;
            }
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Sĩ số phải là số nguyên dương!");
            return;
        }

        // Tạo đối tượng Lop (constructor đủ 10 tham số)
        System.out.println("Mã khoa đang chọn: [" + maKhoa + "]");
        qlsv.enity.Lop lop = new qlsv.enity.Lop(
                maLop, // String
                tenLop, // String
                maKhoa, // String maKhoa (lấy từ ComboBox)
                "", // String tenKhoa (bổ sung nếu có)
                maGV,
                "",
                nienKhoa, // String
                siSo, // int
                ngayBatDau, // Date
                ngayKetThuc // Date
        );

        // Thêm vào DB
        try {
            dao.insert(lop);
            javax.swing.JOptionPane.showMessageDialog(this, "Thêm lớp thành công!");
            lamMoi();
            dispose();
        } catch (Exception ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Thêm lớp thất bại!\n" + ex.getMessage());
        }
    }

    private void suaHocKy() {
        DefaultTableModel model = (DefaultTableModel) tblDsHK.getModel();
        int rowChecked = -1;
        for (int i = 0; i < model.getRowCount(); i++) {
            Boolean checked = (Boolean) model.getValueAt(i, 6); // Cột checkbox
            if (checked != null && checked) {
                rowChecked = i;
                break;
            }
        }
        if (rowChecked == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng chọn học kỳ cần sửa!");
            return;
        }
        // Format ngày
        java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd/MM/yyyy");
        Object ngBDObj = model.getValueAt(rowChecked, 2);
        Object ngKTObj = model.getValueAt(rowChecked, 3);

        String ngBDStr = "";
        String ngKTStr = "";
        if (ngBDObj instanceof java.util.Date) {
            ngBDStr = sdf.format((java.util.Date) ngBDObj);
        } else if (ngBDObj != null) {
            ngBDStr = ngBDObj.toString();
        }
        if (ngKTObj instanceof java.util.Date) {
            ngKTStr = sdf.format((java.util.Date) ngKTObj);
        } else if (ngKTObj != null) {
            ngKTStr = ngKTObj.toString();
        }

        txtMaHK.setText(model.getValueAt(rowChecked, 0) != null ? model.getValueAt(rowChecked, 0).toString() : "");
        txtTenHK.setText(model.getValueAt(rowChecked, 1) != null ? model.getValueAt(rowChecked, 1).toString() : "");
        txtNgBD.setText(ngBDStr);
        txtNgKT.setText(ngKTStr);
        txtNamHoc.setText(model.getValueAt(rowChecked, 4) != null ? model.getValueAt(rowChecked, 4).toString() : "");
        cboTrangThai.setSelectedItem(model.getValueAt(rowChecked, 5) != null ? model.getValueAt(rowChecked, 5).toString() : "Hoạt động");

        txtMaHK.setEnabled(false);
        txtMaHK.setEditable(false);
    }

    private void loadTableSinhVien() {
        DefaultTableModel model = (DefaultTableModel) btlDSsv.getModel();
        model.setRowCount(0); // Xóa dữ liệu cũ

        qlsv.dao.SinhVienDAO svDao = new qlsv.dao.impl.SinhVienDAOImpl();
        qlsv.dao.LopDAO lopDao = new qlsv.dao.impl.LopDAOImpl();

        // Lấy danh sách lớp thuộc khoa này
        List<Lop> danhSachLop = lopDao.selectByMaKhoa(maKhoa);

        // Lấy tất cả sinh viên
        List<qlsv.enity.SinhVien> allSinhVien = svDao.selectAll();

        // Lọc sinh viên thuộc các lớp của khoa này
        for (qlsv.enity.SinhVien sv : allSinhVien) {
            if (sv.getMaLop() != null) {
                // Kiểm tra xem lớp của sinh viên có thuộc khoa này không
                for (Lop lop : danhSachLop) {
                    if (lop.getMaLop().equals(sv.getMaLop())) {

                        // Chuyển đổi giới tính
                        String gioiTinhStr = "Nam";
                        if (sv.getGioiTinh().equals("0") || sv.getGioiTinh().equalsIgnoreCase("false")
                                || sv.getGioiTinh().equalsIgnoreCase("nu") || sv.getGioiTinh().equalsIgnoreCase("nữ")) {
                            gioiTinhStr = "Nữ";
                        }

                        // Chuyển đổi trạng thái
                        String trangThaiStr = "Đang học";
                        if ("1".equals(sv.getTrangThai())) {
                            trangThaiStr = "Nghỉ học";
                        } else if ("2".equals(sv.getTrangThai())) {
                            trangThaiStr = "Tốt nghiệp";
                        }

                        model.addRow(new Object[]{
                            sv.getMaSV(), // Mã số SV
                            sv.getHoTen(), // Họ tên
                            gioiTinhStr, // SỬA: Sử dụng gioiTinhStr đã chuyển đổi
                            sv.getDiaChi(), // Địa chỉ
                            sv.getSdt(), // SDT
                            sv.getEmail(), // Email
                            sv.getMaLop(), // Mã lớp
                            trangThaiStr, // SỬA: Sử dụng trangThaiStr đã chuyển đổi
                            false // Checkbox
                        });
                        break; // Thoát khỏi vòng lặp lớp
                    }
                }
            }
        }
    }

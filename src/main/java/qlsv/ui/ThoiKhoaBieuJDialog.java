    // đổ dữ liệu lên form 
   private void duLieuLenForm() {
    int row = tblTKB.getSelectedRow();
    if (row == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng chọn một dòng để sửa!");
        return;
    }
    // Cột 0: id (ẩn), 1: Thứ, 2: Tiết bắt đầu, 3: Số tiết, 4: Mã môn, 5: Tên môn, 6: Mã GV, 7: Tên GV, 8: Phòng, 9: Ghi chú, 10: Checkbox
    String thuDB = tblTKB.getValueAt(row, 1).toString();
    String thuForm = "";
    switch (thuDB.trim()) {
        case "T2": thuForm = "Thứ 2"; break;
        case "T3": thuForm = "Thứ 3"; break;
        case "T4": thuForm = "Thứ 4"; break;
        case "T5": thuForm = "Thứ 5"; break;
        case "T6": thuForm = "Thứ 6"; break;
        case "T7": thuForm = "Thứ 7"; break;
        default: thuForm = "Thứ 2";
    }
    for (int i = 0; i < cboThuTKB.getItemCount(); i++) {
        if (cboThuTKB.getItemAt(i).trim().equals(thuForm)) {
            cboThuTKB.setSelectedIndex(i);
            break;
        }
    }

    txtTietBD.setText(tblTKB.getValueAt(row, 2).toString());
    txtSoTiet.setText(tblTKB.getValueAt(row, 3).toString());
    txtMaMon.setText(tblTKB.getValueAt(row, 4).toString());
    txtTenMon.setText(tblTKB.getValueAt(row, 5).toString());
    txtMaGV.setText(tblTKB.getValueAt(row, 6).toString());
    txtTenGV.setText(tblTKB.getValueAt(row, 7).toString());
    txtPhong.setText(tblTKB.getValueAt(row, 8).toString());
    txtGhiChu.setText(tblTKB.getValueAt(row, 9).toString());

    // Khóa các trường không được sửa
    txtMaMon.setEditable(false);
    txtTenMon.setEditable(false);
    txtMaGV.setEditable(false);
    txtTenGV.setEditable(false);
    txtMaMon.setEnabled(false);
    txtTenMon.setEnabled(false);
    txtMaGV.setEnabled(false);
    txtTenGV.setEnabled(false);
}

    // sưarr thông tin 
    private void sua() {
        
        int row = tblTKB.getSelectedRow();
        if (row == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng chọn thông tin cần sửa!");
            return;
        }
        int id = Integer.parseInt(tblTKB.getValueAt(row, 0).toString());
        // Kiểm tra không được để trống thông tin
        if (txtTietBD.getText().trim().isEmpty()
                || txtSoTiet.getText().trim().isEmpty()
                || txtPhong.getText().trim().isEmpty()
                || txtMaMon.getText().trim().isEmpty()
                || txtTenMon.getText().trim().isEmpty()
                || txtMaGV.getText().trim().isEmpty()
                || txtTenGV.getText().trim().isEmpty()
                || txtGhiChu.getText().trim().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!");
            return;
        }
        try {
            int tietBD = Integer.parseInt(txtTietBD.getText().trim());
            int soTiet = Integer.parseInt(txtSoTiet.getText().trim());
            if (tietBD <= 0 || soTiet <= 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Tiết bắt đầu và số tiết phải là số nguyên dương!");
                return;
            }
            // Hiển thị hộp thoại chọn học kỳ
            List<String> dsHocKy = tkbDAO.getAllMaHK();
            if (dsHocKy.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Không có học kỳ nào để chọn!");
                return;
            }
            String[] arrHocKy = dsHocKy.toArray(new String[0]);
            String maHK = (String) javax.swing.JOptionPane.showInputDialog(
                    this,
                    "Vui lòng chọn học kỳ:",
                    "Chọn học kỳ",
                    javax.swing.JOptionPane.QUESTION_MESSAGE,
                    null,
                    arrHocKy,
                    arrHocKy[arrHocKy.length - 1]
            );
            if (maHK == null) {
                javax.swing.JOptionPane.showMessageDialog(this, "Bạn chưa chọn học kỳ!");
                return;
            }
            ThoiKhoaBieu tkb = new ThoiKhoaBieu();
            tkb.setId(Integer.parseInt(tblTKB.getValueAt(row, 0).toString()));
            tkb.setThu(convertThuToDB((String) cboThuTKB.getSelectedItem()));
            tkb.setTietBatDau(tietBD);
            tkb.setSoTiet(soTiet);
            tkb.setPhongHoc(txtPhong.getText().trim());
            tkb.setGhiChu(txtGhiChu.getText().trim());
            tkb.setMaMH(txtMaMon.getText().trim());
            tkb.setTenMH(txtTenMon.getText().trim());
            tkb.setMaGV(txtMaGV.getText().trim());
            tkb.setTenGV(txtTenGV.getText().trim());
            tkb.setMaHK(maHK);
            boolean ok = tkbDAO.update(tkb);
            if (ok) {
                String maLop = (String) cboLopTKB.getSelectedItem();
                loadTKBToTable(maLop);
                javax.swing.JOptionPane.showMessageDialog(this, "Sửa thành công!");
                lamMoi();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Sửa thất bại!");
            }
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Tiết bắt đầu và số tiết phải là số!");
        }
        btnLuu.setEnabled(false);
    }

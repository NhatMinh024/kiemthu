// HÀM THÊM LỚP MỚI
private void themLop() {
    // Lấy dữ liệu từ form
    String maLop = txtMaLop.getText().trim();
    String tenLop = txtTenLop.getText().trim();
    String maGV = txtGVCN.getText().trim();
    String nienKhoa = txtNienKhoa.getText().trim();
    String siSoStr = txtSiS.getText().trim();
    String ngayBDStr = txtNgBD.getText().trim();
    String ngayKTStr = txtNgKT.getText().trim();

    // Không được để trống bất kì htoong tin nào 
    if (maLop.isEmpty() || tenLop.isEmpty() || maGV.isEmpty() || nienKhoa.isEmpty()
            || siSoStr.isEmpty() || ngayBDStr.isEmpty() || ngayKTStr.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin bắt buộc!");
        return;
    }

    // Kiểm tra mã giáo viên có tồn tại
    qlsv.dao.NhanVienDAO nvDao = new qlsv.dao.impl.NhanVienDAOImpl();
    if (nvDao.selectById(maGV) == null) {
        javax.swing.JOptionPane.showMessageDialog(this, "Mã giáo viên này không tồn tại!");
        return;
    }

    // Kiểm tra định dạng ngày
    if (!ngayBDStr.matches("\\d{2}-\\d{2}-\\d{4}") || !ngayKTStr.matches("\\d{2}-\\d{2}-\\d{4}")) {
        javax.swing.JOptionPane.showMessageDialog(this,
                "Phải nhập đúng định dạng Ngày-Tháng-Năm (ví dụ: 01-01-2023)!");
        return;
    }

    java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd-MM-yyyy");
    sdf.setLenient(false);
    java.util.Date ngayBD, ngayKT;
    try {
        ngayBD = sdf.parse(ngayBDStr);
        ngayKT = sdf.parse(ngayKTStr);
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this,
                "Ngày không hợp lệ (ví dụ: 01-01-2023)!");
        return;
    }

    // Parse sĩ số
    int siSo;
    try {
        siSo = Integer.parseInt(siSoStr);
        if (siSo <= 0) throw new NumberFormatException();
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Sĩ số phải là số nguyên dương!");
        return;
    }

    // Kiểm tra mã lớp trùng
    qlsv.dao.LopDAO dao = new qlsv.dao.impl.LopDAOImpl();
    if (dao.selectById(maLop) != null) {
        javax.swing.JOptionPane.showMessageDialog(this, "Mã lớp đã tồn tại!");
        return;
    }

    // Tạo đối tượng Lop và thêm mới
    qlsv.enity.Lop lop = new qlsv.enity.Lop(
            maLop, tenLop, maKhoa, "", maGV, "", nienKhoa, siSo,
            new java.sql.Date(ngayBD.getTime()),
            new java.sql.Date(ngayKT.getTime())
    );

    try {
        dao.insert(lop);
        javax.swing.JOptionPane.showMessageDialog(this, "Thêm lớp thành công!");
        this.dispose();
    } catch (Exception ex) {
        javax.swing.JOptionPane.showMessageDialog(this, "Thêm lớp thất bại!\n" + ex.getMessage());
    }
}


private void loadMaKhoaToComboBox() {
    cboKhoa.removeAllItems();
    qlsv.dao.KhoaDAO khoaDao = new qlsv.dao.impl.KhoaDAOImpl();
    for (qlsv.enity.Khoa k : khoaDao.selectAll()) {
        cboKhoa.addItem(k.getMaKhoa());
    }
    // Đảm bảo luôn chọn 1 mã khoa mặc định nếu có
    if (cboKhoa.getItemCount() > 0) {
        cboKhoa.setSelectedIndex(0);
    }
    cboKhoa.setEditable(false);
}

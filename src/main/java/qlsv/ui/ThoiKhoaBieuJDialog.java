    // đổ dữ liệu lên bảng 
    private void loadTKBToTable(String maLop) {
    List<ThoiKhoaBieu> list = tkbDAO.selectByLop(maLop);
    DefaultTableModel model = (DefaultTableModel) tblTKB.getModel();
    model.setRowCount(0);
    tblTKB.setDefaultRenderer(Boolean.class, tblTKB.getDefaultRenderer(Boolean.class));
    for (ThoiKhoaBieu tkb : list) {
        model.addRow(new Object[]{
            tkb.getId(), // cột id
            tkb.getThu(),
            tkb.getTietBatDau(),
            tkb.getSoTiet(),
            tkb.getMaMH(),
            tkb.getTenMH(),
            tkb.getMaGV(),
            tkb.getTenGV(),
            tkb.getPhongHoc(),
            tkb.getGhiChu(),
            Boolean.FALSE // cột checkbox kiểu Boolean
        });
    }
    // Ẩn cột id (cột đầu tiên)
    tblTKB.getColumnModel().getColumn(0).setMinWidth(0);
    tblTKB.getColumnModel().getColumn(0).setMaxWidth(0);
    tblTKB.getColumnModel().getColumn(0).setWidth(0);

    thongKeNhanh(maLop);
}


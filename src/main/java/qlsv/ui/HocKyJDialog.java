// thêm học kỳ 

    private void themHocKy() {
        // Kiểm tra trống
        if (txtMaHK.getText().trim().isEmpty()
                || txtTenHK.getText().trim().isEmpty()
                || txtNgBD.getText().trim().isEmpty()
                || txtNgKT.getText().trim().isEmpty()
                || txtNamHoc.getText().trim().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Không được để trống bất kỳ thông tin nào!");
            return;
        }
        // Kiểm tra trùng mã hoặc tên học kỳ
        DefaultTableModel model = (DefaultTableModel) tblDsHK.getModel();
        String maHK = txtMaHK.getText().trim();
        String tenHK = txtTenHK.getText().trim();
        for (int i = 0; i < model.getRowCount(); i++) {
            String maHKTable = model.getValueAt(i, 0).toString();
            String tenHKTable = model.getValueAt(i, 1).toString().trim();
            if (maHK.equalsIgnoreCase(maHKTable)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Mã học kỳ đã tồn tại, vui lòng nhập mã khác!");
                return;
            }
            if (tenHK.equalsIgnoreCase(tenHKTable)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Tên học kỳ đã tồn tại, vui lòng nhập tên khác!");
                return;
            }
        }
        // Kiểm tra định dạng ngày
        java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd/MM/yyyy");
        sdf.setLenient(false);
        java.util.Date dateBD, dateKT;
        try {
            dateBD = sdf.parse(txtNgBD.getText().trim());
            dateKT = sdf.parse(txtNgKT.getText().trim());
            if (dateKT.before(dateBD)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Ngày kết thúc phải sau ngày bắt đầu!");
                return;
            }
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Ngày bắt đầu và ngày kết thúc phải đúng định dạng Ngày/Tháng/Năm !");
            return;
        }
        // Thêm vào database
        try {
            HocKy hk = new HocKy(
                    maHK,
                    tenHK,
                    dateBD,
                    dateKT,
                    txtNamHoc.getText().trim(),
                    cboTrangThai.getSelectedItem().toString().equals("Hoạt động")
            );
            hocKyDAO.insert(hk);
            javax.swing.JOptionPane.showMessageDialog(this, "Thêm học kỳ thành công!");
            doDuLieu(); // Load lại bảng
            lamMoi();   // Reset form
        } catch (Exception ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Lỗi thêm học kỳ: " + ex.getMessage());
        }
    }

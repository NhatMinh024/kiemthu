public void xoa() {
    // lấy mã sinh viên và vị trí dòng được vchojn 
    // ếu chưa chọn dòng nào thì thông báo lỗi 
    String maSV = txtMaSv.getText().trim();
    DefaultTableModel model = (DefaultTableModel) tblSinhVien.getModel();
    int row = tblSinhVien.getSelectedRow();
    if (row < 0) {
        javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng chọn sinh viên cần xóa!");
        return;
    }
    // nếu trạng thái là đang học thì cũng không xóa được 
    String trangThai = model.getValueAt(row, 8).toString().trim(); // cột 8 là trạng thái
    if ("Đang học".equalsIgnoreCase(trangThai)) {
        javax.swing.JOptionPane.showMessageDialog(this, "Không thể xóa sinh viên có trạng thái 'Đang học'!");
        return;
    }
    // hiển thị hộp thoại có chắc chắn muốn xóa hay không 
    int confirm = javax.swing.JOptionPane.showConfirmDialog(
        this,
        "Bạn có chắc chắn muốn xóa sinh viên này?",
        "Xác nhận xóa",
        javax.swing.JOptionPane.YES_NO_OPTION
    );
    // nếu chọn   khác yes thì return luôn  
    if (confirm != javax.swing.JOptionPane.YES_OPTION) {
        return;
    }
    // xác nhận thì gọi dao để xóa sinh viên luôn 
    SinhVienDAO dao = new SinhVienDAOImpl();
    dao.delete(maSV);
    fillTableSinhVien();
    lamMoiForm(); // Làm mới form sau khi xóa
    javax.swing.JOptionPane.showMessageDialog(this, "Xóa sinh viên thành công!");
}
